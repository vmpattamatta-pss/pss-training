package com.sample.training;

public class ComplexNumber {
	private int real;
	private int img;

	public ComplexNumber() {
		super();
	}

	public ComplexNumber(int real, int img) {
		super();
		this.real = real;
		this.img = img;
	}

	public int getReal() {
		return real;
	}

	public void setReal(int real) {
		this.real = real;
	}

	public int getImg() {
		return img;
	}

	public void setImg(int img) {
		this.img = img;
	}

}
